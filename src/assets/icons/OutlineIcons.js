/**
 * アイコンIDからSVGを取得します。
 * 
 * @param {string} iconId - アイコンID
 * @returns {string} SVGコード（見つからない場合は空文字列）
 */
export function getOutlineIcon(iconId) {
    return OUTLINE_ICONS[iconId]?.svg || '';
}

/**
 * 全アイコンのリストを取得します。
 * 
 * @returns {Array<{id: string, name: string, svg: string}>} アイコン情報の配列
 */
export function getIconList() {
    return Object.entries(OUTLINE_ICONS).map(([id, data]) => ({
        id,
        name: data.name,
        svg: data.svg
    }));
}

/**
 * トグルアイコンを取得します。
 * 
 * @param {boolean} isExpanded - 展開状態かどうか
 * @returns {string} SVGコード
 */
export function getToggleIcon(isExpanded) {
    return isExpanded ? TOGGLE_ICONS.expanded : TOGGLE_ICONS.collapsed;
}
